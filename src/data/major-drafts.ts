// ───── 从中文名称到 (gateSlug, categorySlug) 的反向映射 ─────

const CAT_NAME_TO_PATH: Record<string, { gateSlug: string; categorySlug: string }> = {};
for (const gate of catalog.gates) {
  for (const cat of gate.categories) {
    CAT_NAME_TO_PATH[cat.name] = { gateSlug: gate.slug, categorySlug: cat.slug };
  }
}

function pathFor(name: string): string {
  const p = CAT_NAME_TO_PATH[name];
  if (!p) {
    console.warn(`[major-drafts] 未找到专业类 "${name}" 在目录中的映射，跳过`);
    return '';
  }
  return `${p.gateSlug}/${p.categorySlug}`;
}

